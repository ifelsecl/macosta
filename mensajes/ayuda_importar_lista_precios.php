Puedes usar un archivo en formato <b>CSV</b> (Comma Separated Values) o <b>TXT</b> (Texto Plano). <br />
El archivo debe tener 5 columnas cada una separada por una coma.<br>
ej: 0810000,081000,1,8500,2
<ol style="text-align:left !important;">
	<li><b>Código ciudad origen</b>: el código de la ciudad origen según tabla DANE.</li>
	<li><b>Código ciudad destino</b>: el código de la ciudad destino según tabla DANE.</li>
	<li><b>Código del embalaje</b>: lo puedes encontrar en Configuracion->Embalajes.</li>
	<li><b>Precio</b>: el precio que se quiere cobrar.</li>
	<li><b>Seguro</b>: el porcentaje de seguro cobrado a la ciudad destino.</li>
</ol>
